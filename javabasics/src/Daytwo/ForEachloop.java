package Daytwo;

public class ForEachloop {

	public static void main(String[] args) {
		int[] a= {15,16,17,18,19};
		for(int i:a)
		{
			System.out.println(i);
		}
		
		char[] b= {'j','a','v','a'};
		for (char j:b)
		{
			System.out.print(j);
		}
		System.out.println(" ");
		String[] c= {"java","programming","Language"};
		for(String k:c)
		{
			System.out.print(k+" ");
		}
	}

}
