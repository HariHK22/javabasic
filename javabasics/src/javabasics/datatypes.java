package javabasics;

public class datatypes {

		public static void main (String[] args)
		{
			
			int a = 50 / 100;
			float b=50/62.f;
			double c=50d/6.6d;
			int marks =510;
			
			double percentage = marks *0.25d;
			
			System.out.println("The integer division is :"+a);
			System.out.println("The float divison is :"+b);
			System.out.println("The double divison is :"+c);
			System.out.println("The mark percentage is :"+percentage);
		}
	

}
